    /*
    A estrutura de repetição for tem um número pré-determinado de vezes em que deve executar, assim como while, ela verifica a condição do loop antes de executar as instruções lógicas.
    A diferença entre ambas, é que while é regido por um variável geralmente true, false ou uma comparação, enquanto essa condição se satisfazer, o loop será executado, já o for irá parar depois de N repetições já estabelecidas no ínicio.
    
    do while tem um comportamento semelhante ao for, mas dentro do bloco do ele executará primeiro a instrução lógica e depois irá validar a condição de permanência do loop.
    
    while verifica a condição primeiro e depois executa as instruções, do while executa as instruções e depois verifica a condição para continuar.
     */
